// build-standalone.mjs — bundles gallery JS into a single index.htm.
// Three.js and Google Fonts load from CDN (pinned versions).
// PointerLockControls is inlined (transformed to use window.THREE).
//
//   run:  node gallery/build-standalone.mjs      (from repo root)
//    or:  cd gallery && node build-standalone.mjs

import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const here = dirname(fileURLToPath(import.meta.url));
const read = f => readFileSync(join(here, f), 'utf8');

async function fetchText(url, headers = {}) {
  const res = await fetch(url, { headers });
  if (!res.ok) throw new Error(`HTTP ${res.status} fetching ${url}`);
  return res.text();
}

// Escape `</script` inside inline <script> blocks so the HTML parser doesn't
// terminate the tag early when the JS contains that string in a literal.
function escInlineScript(js) {
  return js.replace(/<\/script/gi, '<\\/script');
}

// ── 1. CDN URLs (pinned versions) ────────────────────────────────────────────
const THREE_CDN = 'https://cdn.jsdelivr.net/npm/three@0.160.0/build/three.min.js';
// Google Fonts link tags are kept as-is from page.htm (CDN, specific families/weights).

// ── 2. PointerLockControls — inline, transformed to use global THREE ─────────
console.log('Fetching PointerLockControls...');
let plc = await fetchText('https://cdn.jsdelivr.net/npm/three@0.160.0/examples/jsm/controls/PointerLockControls.js');
// Replace `import { Foo, Bar } from 'three'` with `const { Foo, Bar } = THREE`
plc = plc.replace(
  /^import\s*\{([^}]+)\}\s*from\s*['"]three['"];?\s*$/m,
  (_, names) => `const {${names.trim()}} = THREE;`
);
// Strip the module export; expose the class as a browser global
plc = plc.replace(/^export\s*\{[^}]*\};?\s*$/m, '');
plc += '\nwindow.PointerLockControls = PointerLockControls;';
console.log(`  PointerLockControls: ${(plc.length / 1024).toFixed(0)} KB`);

// ── 3. Gallery JS bundle ──────────────────────────────────────────────────────
// nd.js: strip `export` keywords so names become bundle-scope locals
const nd = read('nd.js').replace(/^export\s+/gm, '');

// exhibits.js: drop its import + ND. namespace + export
const exhibits = read('exhibits.js')
  .replace(/^import\s+\*\s+as\s+ND\s+from\s+['"]\.\/nd\.js['"];?\s*$/m, '')
  .replace(/\bND\./g, '')
  .replace(/^export\s+/gm, '');

// main.js: drop ALL import lines (three imports are now globals; local imports now in scope)
const mainLines = read('main.js')
  .split('\n')
  .filter(l => !/^\s*import\s/.test(l));

const galleryBundle = [
  '/* GENERATED by gallery/build-standalone.mjs — edit gallery/*.js, not this block */',
  '/* ===== nd.js ===== */',
  nd,
  '/* ===== exhibits.js ===== */',
  exhibits,
  '/* ===== main.js ===== */',
  mainLines.join('\n'),
].join('\n');

// ── 4. Assemble the page ──────────────────────────────────────────────────────
let html = read('page.htm');

// Remove the importmap (we're using the UMD build instead of ES modules)
html = html.replace(/<script type="importmap">[\s\S]*?<\/script>\n?/m, '');

// Replace the bundle marker.
// - Three.js from CDN via <script src> (UMD build sets window.THREE).
// - PointerLockControls inlined as plain <script> (uses window.THREE).
// - Gallery bundle as <script defer> (not type="module") — works from file://.
// - escInlineScript() prevents `</script` in literals from cutting the tag short.
const replacement = [
  `<script src="${THREE_CDN}"></script>`,
  '<script>',
  '/* three/addons/controls/PointerLockControls.js — MIT License */',
  escInlineScript(plc),
  '</script>',
  '<script defer>',
  escInlineScript(galleryBundle),
  '</script>',
].join('\n');

html = html.replace('<!--GALLERY_BUNDLE-->', replacement);

// ── 5. Write output ───────────────────────────────────────────────────────────
const outPath = join(here, '..', 'index.htm');
writeFileSync(outPath, html);

const sizeMB = (html.length / 1024 / 1024).toFixed(2);
console.log(`\nBuilt ${outPath}`);
console.log(`  Total size:   ${sizeMB} MB`);
console.log(`  nd.js:        ${(nd.length / 1024).toFixed(0)} KB`);
console.log(`  exhibits.js:  ${(exhibits.length / 1024).toFixed(0)} KB`);
console.log(`  main.js body: ${(mainLines.join('\n').length / 1024).toFixed(0)} KB`);
console.log(`  Three.js CDN: ${THREE_CDN}`);
console.log(`  Fonts CDN:    fonts.googleapis.com (from page.htm link tags)`);
